#include <stdio.h>


/*

Escreva um algoritmo que imprima todos os números de 1 até 100. Elabore ao menos versões distintas
(códigos/estratégias diferentes) para este algoritmo
*/

int main()
{
    int numero = 0;

    for (numero = 0; numero <= 100; numero ++)
    {
        printf("Seu Numero eh: \n%d\n", numero);
    }
    return 0;
}